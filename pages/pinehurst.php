            <h4>AN UPDATE ON THE PINEHURST DRIVE</h4>
                <br /><br />
                Dear Donors,
                <br /><br />
                The Laurel Little Theatre Board of Trustees thanks you for your generous gift or pledge
                toward our “Raise the Roof” Capital Campaign.  As of this Fall 2014, we are a quarter of the way to our
                financial goal and want to report on our progress.
                <br /><br />
                While we are not sufficiently funded to begin construction, we are fully resolved to continue our fund
                raising efforts and hope to maintain and perhaps increase our momentum.  In that light we ask that if you
                have made a pledge to please consider raising that pledge and/or extending it another year or two so that
                we can count on that funding.
                <br /><br />
                Our plan is to construct the 150' by 19' addition to the south side of the theatre.  This addition will
                run the entire length of the building from the front door to the back parking area and vertically from
                street level to the roof.  Within these walls and on three levels, LLT will add new modern bathrooms,
                property and set storage, dressing rooms, wing space to move pieces on and off stage during productions
                and The Pinehurst Room for meetings and to relieve patron crowding before shows.
                <br /><br />
                We still need to raise more funds to complete the whole project, but we owe it to those who have already
                given to restate our commitment to raising the needed funds.  The Board of Trustees hopes to break ground
                by the summer of 2016 and will keep everyone informed of our progress going forward.
                <br /><br />
                Thank you for caring about "community theater" in Laurel/Jones County and know that your financial gift
                will put LLT on firm footing for the next generation of patrons.  We look forward to seeing you this year
                for our 54th season.
                <br /><br />
                Sincerely,<br /><br />
                George Jackson, Stan Riles, Tommy Thames, Frankie Bennett &amp; Missy Sanderson
